package com.example.alpha;

public class ExtraCurricularsItem {

    String title, description;

    public ExtraCurricularsItem() {}

    public ExtraCurricularsItem(String title, String description) {

        this.title = title;
        this.description = description;
    }

    public String getTitle()
    {
        return this.title;
    }

    public String getDescription()
    {
        return this.description;
    }

}
